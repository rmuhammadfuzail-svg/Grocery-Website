let cart = [];

function login() {
  let name = document.getElementById("name").value;
  let email = document.getElementById("email").value;
  let password = document.getElementById("password").value;

  if (name === "" || email === "" || password === "") {
    alert("Please fill all fields");
    return;
  }

  if (!email.endsWith("@gmail.com")) {
    alert("Email must contain @gmail.com");
    return;
  }

  if (password.length < 6) {
    alert("Password must be at least 6 characters");
    return;
  }

  document.getElementById("loginPage").style.display = "none";

  document.getElementById("dashboard").style.display = "block";

  document.getElementById("userName").innerText = name;

  document.getElementById("userEmail").innerText = email;

  displayProducts(products);

  displayCategories();
}

function displayProducts(items) {
  let productsDiv = document.getElementById("products");

  productsDiv.innerHTML = "";

  items.forEach(function (product) {
    productsDiv.innerHTML += `
    
    <div class="card">

      <img src="${product.image}" alt="">

      <h3>${product.name}</h3>

      <p>Rs ${product.price}</p>

      <button onclick="addToCart(${product.id})">
        Add To Cart
      </button>

    </div>
    
    `;
  });
}

function displayCategories() {
  let categoryDiv = document.getElementById("categoryList");

  let categories = [];

  products.forEach(function (product) {
    if (!categories.includes(product.category)) {
      categories.push(product.category);
    }
  });

  categoryDiv.innerHTML = `<button onclick="displayProducts(products)">All</button>`;

  categories.forEach(function (category) {
    categoryDiv.innerHTML += `
    
    <button onclick="filterCategory('${category}')">
      ${category}
    </button>
    
    `;
  });
}

function filterCategory(category) {
  let filteredProducts = products.filter(function (product) {
    return product.category === category;
  });

  displayProducts(filteredProducts);
}

function searchProduct() {
  let value = document.getElementById("search").value.toLowerCase();

  let filtered = products.filter(function (product) {
    return product.name.toLowerCase().includes(value);
  });

  displayProducts(filtered);
}

function addToCart(id) {
  let product = products.find(function (item) {
    return item.id === id;
  });

  cart.push(product);

  updateCart();
}

function updateCart() {
  let cartItems = document.getElementById("cartItems");

  cartItems.innerHTML = "";

  let total = 0;

  cart.forEach(function (item) {
    total = total + item.price;

    cartItems.innerHTML += `
    
    <div class="cart-item">

      <h4>${item.name}</h4>

      <p>Price: Rs ${item.price}</p>

    </div>
    
    `;
  });

  document.getElementById("totalPrice").innerText = total;

  document.getElementById("cartCount").innerText = cart.length;
}

function toggleCart() {
  document.getElementById("cartBox").classList.toggle("active");
}
